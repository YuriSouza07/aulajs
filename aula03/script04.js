/*Monte um programa para ler um valor e informar se o valor é maior do que 10.
caso o valor não seja, informe valor menor ou igual.
*/
let valor =Number(prompt("Digite o valor"));
if (valor > 10) {
    document.write('Valor informado é maior quw 10');
} else {
    document.write('Valor informado é menor ou igual a 10');
}
