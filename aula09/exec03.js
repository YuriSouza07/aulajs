/*Receba um número decimal qualquer e exiba: 
O valor arredondado para cima (Math.ceil()) 
O valor arredondado para baixo (Math.floor()) 
O valor arredondado normalmente (Math.round())
*/
let x = parseFloat(prompt("Informe um número decimal: "));
alert('Valor arredondado para cima: ' + Math.ceil(x));
alert('Valor arredondado para baixo: ' + Math.floor(x));
alert('Valor arredondado normalmente: ' + Math.round(x));   

 